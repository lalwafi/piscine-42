#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	k;

	i = 1;
	while (ac > i)
	{
		k = 0;
		while (av[i][k] != '\0')
		{
			k++;
		}
		write(1, av[i], k);
		write(1, "\n", 1);
		i++;
	}
	return (0);
}
